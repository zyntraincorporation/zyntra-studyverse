// Cycle to next status
export function getNextStatus(current) {
  const idx = STATUS_ORDER.indexOf(current);
  if (idx === -1) return 'in_progress';
  // At revised_5, stay at revised_5 (no accidental reset)
  if (idx >= STATUS_ORDER.length - 1) return STATUS_ORDER[STATUS_ORDER.length - 1];
  return STATUS_ORDER[idx + 1];
}

// Get revision count from status string
export function getRevisionCount(status) {
  if (!status?.startsWith('revised_')) return 0;
  const n = parseInt(status.replace('revised_', ''), 10);
  return isNaN(n) ? 0 : n;
}

// Migrate legacy 'revised' → 'revised_1'
export function normalizeLegacyStatus(status) {
  if (status === 'revised') return 'revised_1';
  return status || 'not_started';
}

